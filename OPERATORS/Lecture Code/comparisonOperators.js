// Comparison Operators

let a = 10;
let b = 20;

console.log(a == "10"); // Equal to: true (type coercion)
console.log(a === "10"); // Strict Equal to: false (no type coercion)
console.log(a != 20); // Not equal to: true
console.log(a !== "10"); // Strict Not equal to: true
console.log(a > b); // Greater than: false
console.log(a < b); // Less than: true
console.log(a >= 10); // Greater than or equal to: true
console.log(a <= b); // Less than or equal to: true
