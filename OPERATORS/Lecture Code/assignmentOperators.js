// Assignment Operators

let x = 10;

x += 5;
console.log(x); // Addition Assignment: 15

x -= 5;
console.log(x); // Subtraction Assignment: 10

x *= 2;
console.log(x); // Multiplication Assignment: 20

x /= 4;
console.log(x); // Division Assignment: 5

x %= 3;
console.log(x); // Modulus Assignment: 2
